  let currentLevelIndex = 0;
  let currentQuestion = 0;
  let lives = 3;
  let quizData = null;
  
  const questionText = document.getElementById("questionText");
  const questionImage = document.getElementById("questionImage");
  const answersGrid = document.getElementById("answersGrid");
  const justificationBox = document.getElementById("justificationBox");
  const gameOverBox = document.getElementById("gameOver");
  const levelCompleteBox = document.getElementById("levelCompleteBox");
  const levelCompleteText = document.getElementById("levelCompleteText");
  
  function loadLevel(index) {
    quizData = levelData.levels[index];
    document.querySelector(".quiz-canva").style.backgroundImage = `url('${quizData.background}')`;
    currentQuestion = 0;
    updateHearts();
    loadQuestion();
  }
  
  function loadQuestion() {
    const q = quizData.questions[currentQuestion];
    questionText.textContent = q.Question;
  
    if (q.Question_img_path) {
      questionImage.src = q.Question_img_path;
      questionImage.style.display = "block";
    } else {
      questionImage.style.display = "none";
    }
  
    document.getElementById("questionCounter").textContent = `${currentQuestion + 1}/${quizData.questions.length}`;
    answersGrid.innerHTML = "";
    justificationBox.style.display = "none";
  
    const answers = q.answers[0];
    const imgPaths = q.answer_img_paths[0];
    Object.keys(answers).forEach((label, i) => {
      const isCorrect = answers[label];
      const btn = document.createElement("div");
      btn.className = "answer-btn";
      btn.innerHTML = `<div>${label}</div>`;
      const img = imgPaths[`answer${i+1}`];
      if (img) {
        const imgTag = document.createElement("img");
        imgTag.src = img;
        imgTag.style.maxHeight = "80px";
        btn.appendChild(imgTag);
      }
      btn.onclick = () => checkAnswer(btn, isCorrect, q.justification);
      answersGrid.appendChild(btn);
    });
  }
  
  function checkAnswer(btn, isCorrect, justification) {
    document.querySelectorAll(".answer-btn").forEach(b => b.onclick = null);
    btn.classList.add(isCorrect ? "correct" : "wrong");
    if (!isCorrect) loseLife();
    justificationBox.textContent = justification;
    justificationBox.style.display = "block";
  
    setTimeout(() => {
      if (lives <= 0) return;
      enableContinue();
    }, 3000);
  }
  
  function enableContinue() {
    const proceed = () => {
      document.querySelector(".quiz-canva").removeEventListener("click", proceed);
      window.removeEventListener("keydown", keyFn);
      currentQuestion++;
      if (currentQuestion >= quizData.questions.length) {
        levelCompleteText.textContent = currentLevelIndex + 1 < levelData.levels.length
          ? `yoCaR! Semdegi turi: ${levelData.levels[currentLevelIndex+1].name}`
          : `TamaSi dasrulda!`;
        levelCompleteBox.style.display = "block";
      } else {
        loadQuestion();
      }
    };
    const keyFn = (e) => { if (e.code === "Space") proceed(); };
    document.querySelector(".quiz-canva").addEventListener("click", proceed);
    window.addEventListener("keydown", keyFn);
  }
  
  function loseLife() {
    lives--;
    updateHearts();
    if (lives <= 0) gameOverBox.style.display = "block";
  }
  
  function updateHearts() {
    document.querySelectorAll(".heart-icon").forEach((heart, i) => {
      heart.style.opacity = i < lives ? "1" : "0.3";
    });
  }
  
  function retryLevel() {
    lives = 1;
    gameOverBox.style.display = "none";
    updateHearts();
    loadQuestion();
  }
  
  function nextLevel() {
    levelCompleteBox.style.display = "none";
    currentLevelIndex++;
    if (currentLevelIndex < levelData.levels.length) {
      lives = Math.min(3, lives + 1);
      loadLevel(currentLevelIndex);
    } else {
      document.getElementById("gameEndScreen").style.display = "flex";
    }
  }
  function goToStartPage() {
    window.location.href = "index.html";
  }
  
  
  loadLevel(currentLevelIndex);

  